package AccesesModifier;

public class Access1 
{
//public access modifier you can accesses any package or any  class
//	lets see the example
	public String name="max";
	public int age=5;
	public String color="brown";
	//if you use  protected access modifier before data type then your variable visibility in same package dont use another package
	
	protected String name1="tysen";
	protected int age1=6;
	protected String color1="black";
	
	//	//if you use  private access modifier  before data type then your variable visibility in same class dont accesses outside the class
	
	private String name2="rocky";
	private int age2=7;
	private String color3="white";
	
	//and last one is default access modifier so this modifier visibility like public accesses modifier like you can accesses any where 
	
	String name4="jack";
	int age4 =8;
	String color4="dark brown";
}
